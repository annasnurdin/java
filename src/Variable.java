public class Variable {
    public static void main(String[] args) {
        String name;
        name = "Annas Nurdin"; // define variabel bisa terpisah dengan pengisian value

        System.out.println(name);

        var nama = "Annas Aja"; // kalau var, harus sebaris deklarasi dan isi value;
        System.out.println(nama);

        final String sukses = "Ini variabel yang final, tidak bisa diubah, seperti const";
        System.out.println(sukses);
        
    }
}
